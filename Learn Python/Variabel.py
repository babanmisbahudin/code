# Vartiabel adalah tempat untuk menyimpan data dalam program.

a = 10
x = 20

# Pemanggilan variabel Pertama
print( "nilai a adalah", a )
print( "nilai b adalah", x )
print( "nilai a + b adalah", a + x )

# Penamaan dalam variabel
nilai_y = 100
juta10 = 10000000
nilaiZ = 20.5

print( "nilai_y adalah", nilai_y )
print( "juta10 adalah", juta10 )
print( "nilaiZ adalah", nilaiZ )
# Penamaan variabel tidak boleh dimulai dengan angka

# Pemanggilan variabel kedua
print("Nilai a =", a)
