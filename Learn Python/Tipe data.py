#a = 10, a adalah variabel yang menyimpan nilai 10

# tipe data: integer
data_integer = 1
print(type(data_integer))  # Output: <class 'int'>
print("Data :", data_integer, ", bertipe :", type(data_integer))

# tipe data: float (Angka dengan koma   )
data_float = 1.5
print(type(data_float))  # Output: <class 'float'>
print("Data :", data_float, ", bertipe :", type(data_float))

# tipe data: string (Kumpulan karakter)
data_string = "Hello, Baban Misbahudin"
print(type(data_string))  # Output: <class 'str'>
print("Data :", data_string, ", bertipe :", type(data_string))

# tipe data: boolean (True/False)
data_boolean = True
print(type(data_boolean))  # Output: <class 'bool'>
print("Data :", data_boolean, ", bertipe :", type(data_boolean))

# tipe data: khusus "Kompleks" (Angka dengan bagian real dan imajiner)
data_kompleks = 1 + 2j
print(type(data_kompleks))  # Output: <class 'complex'> 
print("Data :", data_kompleks, ", bertipe :", type(data_kompleks))

# tipe data: khusus "None" (Tidak ada nilai)   
data_none = None
print(type(data_none))  # Output: <class 'NoneType'>   
print("Data :", data_none, ", bertipe :", type(data_none))

# tipe data: khusus "Bytes" (Urutan byte)
data_bytes = b"Hello"
print(type(data_bytes))  # Output: <class 'bytes'>
print("Data :", data_bytes, ", bertipe :", type(data_bytes))
