a = 10
b = 3

# Oprasi Penambahan +
hasil = a + b
print(a,'+',b,'=',hasil)

# Oprasi Pengurangan -
hasil = a - b
print(a,'-',b,'=',hasil)

# Oprasi Perkalian *
hasil = a * b
print(a,'*',b,'=',hasil)

# Oprasi Perbagian /
hasil = a / b
print(a,'/',b,'=',hasil)

# Oprasi Eksponen (Pangkat) **
hasil = a ** b
print(a,'**',b,'=',hasil)

# Oprasi Modulus (sisa pembagian) %
hasil = a % b
print(a,'%',b,'=',hasil)

# Operasi Floor Devision //
hasil = a // b
print(a,'//',b,'=',hasil)