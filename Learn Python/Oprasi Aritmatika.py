a = 10
b = 50

# Oprasi Penambahan +
hasil = a + b
print(a,'+',b,'=',hasil)

# Oprasi Pengurangan -
hasil = b - a
print(b,'-',a,'=',hasil)

# Oprasi Perkalian *
hasil = b * a
print(b,'*',a,'=',hasil)

# Oprasi Perbagian /
hasil = b / a
print(b,'/',a,'=',hasil)

# Oprasi Eksponen (Pangkat) **
hasil = b ** a
print(b,'**',a,'=',hasil)

