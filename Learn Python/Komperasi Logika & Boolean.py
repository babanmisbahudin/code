# Membuat gabungan area rentang dan angka

inputUser = float(input("Masukkan angka yang bernilai\nkurang dari 3 \natau \nlebih besar dari 10:"))

# Memeriksa angka kurang dari 3
isKurangDari = (inputUser < 3)
print(isKurangDari)

# Memeriksa angka lebih besar dari 10
isLebihBesarDari = (inputUser > 10)
print(isLebihBesarDari)